# for loops iterate over a given sequence.
brands = ["Apple", "Samsung", "Xiaomi"]
for brand in brands:
  print(brand)

# sum()' function returns a number, the sum of all items in an iterable.
count = (1, 2, 3, 4, 5)
total = sum(count)
print(total)

# for loops iterating through a given range.
total = 0
for number in range(0, 101):
  total += number
print(total)